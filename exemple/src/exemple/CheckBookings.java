    public void showBookInfo(int i)
    {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String str;
        String str2;
        String dateString;
        
        if(valid)
        {
            for(int j=0; j<chosenBooks.size(); j++)
            {
                if(chosenBooks.get(j).getId() == i)
                {
                    dateString = format.format(chosenBooks.get(j).getDate());
                    str = "First Name : " + chosenBooks.get(j).getFirstName() + "\n";
                    str += "Name : " + chosenBooks.get(j).getName()+ "\n";
                    str += "Ride : " + chosenBooks.get(j).getRide()+ "\n";
                    str += "Date : " + dateString + "\n";
                    str += "Id : " + chosenBooks.get(j).getId() + "\n";
                    str += "Price : " + chosenBooks.get(j).getPrice() + "\n";
                    str += "Tickets: " + chosenBooks.get(j).getTickets() + "\n";
                    JOptionPane jop = new JOptionPane();
                    jop.showMessageDialog(null, str, "Booking", JOptionPane.INFORMATION_MESSAGE);
                }
            }   
        }

        else
        {
            for(int j=0; j<books.size(); j++)
            {
                if(books.get(j).getId() == i)
                {
                    dateString = format.format(books.get(j).getDate());
                    str2 = "First Name : " + books.get(j).getFirstName() + "\n";
                    str2 += "Name : " + books.get(j).getName()+ "\n";
                    str2 += "Ride : " + books.get(j).getRide()+ "\n";
                    str2 += "Date : " + dateString + "\n";
                    str2 += "Id : " + books.get(j).getId() + "\n";
                    str2 += "Price : " + books.get(j).getPrice() + "\n";
                    str2 += "Tickets: " + books.get(j).getTickets() + "\n";
                    JOptionPane jop = new JOptionPane();
                    jop.showMessageDialog(null, str2, "Booking", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
        
    }
    
    public void initListe()
    {
        valid = true;
        chosenBooks.clear();
        int k = 0;
        String str = "No booking found for : "+firstName+" "+name;
        for(int i=0; i<books.size(); i++)
        {
            if((name.equals(books.get(i).getName())) && (firstName.equals(books.get(i).getFirstName())))
            {
                k++;
                chosenBooks.add(books.get(i));
            }
        }
        if(k==0)
        {
            JOptionPane jop1 = new JOptionPane();
            jop1.showMessageDialog(null,str , "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        Object[] ids = new Object[k];
        for(int i =0; i<chosenBooks.size(); i++)
        {
            ids[i]=chosenBooks.get(i).getId();
        }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(ids));        
    }
    
